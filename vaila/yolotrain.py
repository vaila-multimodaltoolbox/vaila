    @staticmethod
    def _detect_dataset_split_paths(dataset_path):
        """Detect vailá or FIFA/Ultralytics split folder layouts."""
        vaila_train = os.path.join(dataset_path, "train", "images")
        vaila_val = os.path.join(dataset_path, "val", "images")
        fifa_train = os.path.join(dataset_path, "images", "train")
        fifa_val = os.path.join(dataset_path, "images", "val")
        if os.path.isdir(vaila_train) and os.path.isdir(vaila_val):
            return {
                "layout": "vaila",
                "train": vaila_train,
                "val": vaila_val,
                "test": os.path.join(dataset_path, "test", "images"),
            }
        if os.path.isdir(fifa_train) and os.path.isdir(fifa_val):
            return {
                "layout": "fifa",
                "train": fifa_train,
                "val": fifa_val,
                "test": os.path.join(dataset_path, "images", "test"),
            }
        return None

    def _auto_detect_and_create_yaml(self, dataset_path):
        """Detect YOLO folder layout and create data.yaml."""
        split_paths = self._detect_dataset_split_paths(dataset_path)
        if not split_paths:
            messagebox.showerror(
                "YOLO Dataset Not Found",
                "Expected one of these layouts:\n\n"
                "  train/images + train/labels\n"
                "  val/images   + val/labels\n\n"
                "or\n\n"
                "  images/train + labels/train\n"
                "  images/val   + labels/val\n\n"
                f"Selected folder:\n{dataset_path}",
            )
            return False

        class_names = _read_classes_txt(dataset_path)
        if not class_names:
            class_names = ["object"]

        train_path = os.path.abspath(split_paths["train"]).replace("\\", "/")
        val_path = os.path.abspath(split_paths["val"]).replace("\\", "/")
        test_path_raw = split_paths["test"]
        test_path = (
            os.path.abspath(test_path_raw).replace("\\", "/")
            if test_path_raw and os.path.isdir(test_path_raw)
            else None
        )

        yaml_content = self._create_comprehensive_yaml(
            train_path,
            val_path,
            test_path,
            class_names,
            task="detect",
        )
        yaml_file_path = os.path.join(dataset_path, "data.yaml")

        try:
            with open(yaml_file_path, "w", encoding="utf-8") as f:
                f.write(yaml_content)

            self._set_yaml_selection(yaml_file_path, "Create YAML")
            train_count = _image_count(split_paths["train"])
            val_count = _image_count(split_paths["val"])

            messagebox.showinfo(
                "Success",
                "Dataset ready for training.\n\n"
                f"Classes: {len(class_names)}\n"
                f"Train images: {train_count}\n"
                f"Val images: {val_count}\n"
                f"YAML: {yaml_file_path}",
            )

            return True

        except Exception as e:
            messagebox.showerror("Error", f"Failed to create YAML: {str(e)}")
            return False

