    def _update_model_list(self, event=None):
        """Filter model suggestions by selected task while keeping free text paths allowed."""
        task = self.task_var.get()
        if task == "pose":
            models = [AUTO_MODEL_LABEL] + [m for m in self.available_models if "-pose" in m]
        elif task == "segment":
            models = [AUTO_MODEL_LABEL] + [m for m in self.available_models if "-seg" in m]
        elif task == "detect":
            models = [AUTO_MODEL_LABEL] + [
                m
                for m in self.available_models
                if m != AUTO_MODEL_LABEL and "-pose" not in m and "-seg" not in m
            ]
        else:
            models = self.available_models

        current = self.base_model.get()
        if _is_custom_weights_path(current) and current not in models:
            models = [current, *models]

        self.model_combo["values"] = models
        if (
            current not in models
            and not _is_custom_weights_path(current)
            and current not in {"auto", AUTO_MODEL_LABEL}
        ):
            self.base_model.set(AUTO_MODEL_LABEL)
        self._update_task_guidance()

