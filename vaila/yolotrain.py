def _load_yaml(yaml_file):
    """Load a YOLO data.yaml file."""
    import yaml

    with open(yaml_file, encoding="utf-8") as f:
        data = yaml.safe_load(f) or {}
    if not isinstance(data, dict):
        raise ValueError(f"Invalid YAML mapping: {yaml_file}")
    return data


def _yaml_names_to_list(names):
    """Normalize Ultralytics ``names`` list/dict/string to a list of class names."""
    if isinstance(names, dict):
        out = []
        for key in sorted(names, key=lambda value: int(value) if str(value).isdigit() else value):
            value = str(names[key]).strip()
            if value:
                out.append(value)
        return out
    if isinstance(names, (list, tuple)):
        return [str(item).strip() for item in names if str(item).strip()]
    if isinstance(names, str):
        text = names.strip()
        return [text] if text else []
    return []


def _read_classes_txt(dataset_path):
    classes_file = os.path.join(dataset_path, "classes.txt")
    if not os.path.exists(classes_file):
        return []
    try:
        with open(classes_file, encoding="utf-8") as f:
            return [line.strip() for line in f if line.strip()]
    except OSError:
        return []


def _default_device():
    """Prefer CUDA/MPS when available; otherwise use CPU."""
    if torch.cuda.is_available():
        return "0"
    mps = getattr(getattr(torch, "backends", None), "mps", None)
    if mps is not None:
        with contextlib.suppress(Exception):
            if mps.is_available():
                return "mps"
    return "cpu"


def _resolve_yaml_path_entry(yaml_data, dataset_dir, key):
    """Resolve an Ultralytics train/val/test path entry."""
    raw = yaml_data.get(key, "")
    if isinstance(raw, (list, tuple)):
        for item in raw:
            value = str(item).strip()
            if value:
                break
        else:
            value = ""
    else:
        value = str(raw).strip()
    if not value:
        return value
    if os.path.isabs(value):
        return value
    base = str(yaml_data.get("path") or dataset_dir).strip() or dataset_dir
    if not os.path.isabs(base):
        base = os.path.join(dataset_dir, base)
    return os.path.normpath(os.path.join(base, value.removeprefix("./")))


def _image_count(path):
    if not path or not os.path.isdir(path):
        return 0
    return len([name for name in os.listdir(path) if name.lower().endswith(IMAGE_EXTENSIONS)])


def _label_dir_for_images(image_dir):
    path = pathlib.Path(image_dir)
    if path.name == "images":
        return path.parent / "labels"
    if path.parent.name == "images":
        return path.parent.parent / "labels" / path.name
    return path.parent / "labels"


def _first_label_line(yaml_data, dataset_dir):
    """Return first non-empty label line from train/val labels, if any."""
    for split in ("train", "val"):
        image_dir = _resolve_yaml_path_entry(yaml_data, dataset_dir, split)
        label_dir = _label_dir_for_images(image_dir)
        if not label_dir.is_dir():
            continue
        for txt_path in sorted(label_dir.glob("*.txt")):
            with contextlib.suppress(OSError), open(txt_path, encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if line:
                        return line
    return ""


def _model_task(model_name):
    stem = pathlib.Path(str(model_name)).name.lower()
    if "-pose" in stem:
        return "pose"
    if "-seg" in stem:
        return "segment"
    return "detect"


def _looks_like_named_yolo_model(model_name):
    """Return True for named Ultralytics family weights where suffix implies task."""
    stem = pathlib.Path(str(model_name)).name.lower()
    return stem.startswith(("yolo", "yolov")) and stem.endswith(".pt")


def _suggest_model_for_task(dataset_task, model_name):
    """Suggest same-family YOLO model suffix for the dataset task when possible."""
    name = pathlib.Path(str(model_name)).name
    lower = name.lower()
    if lower.endswith(".pt") and _looks_like_named_yolo_model(name):
        base = name[:-3]
        for suffix in ("-pose", "-seg"):
            if base.lower().endswith(suffix):
                base = base[: -len(suffix)]
                break
        if dataset_task == "pose":
            return f"{base}-pose.pt"
        if dataset_task == "segment":
            return f"{base}-seg.pt"
        return f"{base}.pt"
    return DEFAULT_MODEL_BY_TASK.get(dataset_task, DEFAULT_MODEL_BY_TASK["detect"])


def _validate_model_task_match(dataset_task, model_name):
    """Fail early when a known YOLO model family does not match the dataset task."""
    model_task = _model_task(model_name)
    if dataset_task == model_task or not _looks_like_named_yolo_model(model_name):
        return

    expected = _suggest_model_for_task(dataset_task, model_name)
    if dataset_task == "pose":
        detail = (
            "This dataset has kpt_shape / YOLO-pose labels. A detect model will read "
            "keypoint visibility values (2) as coordinates and Ultralytics will report "
            "'non-normalized or out of bounds coordinates [2. 2.]'."
        )
    elif dataset_task == "segment":
        detail = "This dataset appears to contain polygon labels and needs a *-seg.pt model."
    else:
        detail = "This dataset contains bbox detection labels and needs a detection model."
    raise ValueError(
        f"Model/task mismatch: dataset task is '{dataset_task}' but model '{model_name}' "
        f"is a '{model_task}' model. Use --model {expected} or --model auto. {detail}"
    )


def _detect_task_from_yaml(yaml_data, dataset_dir):
    """Detect Ultralytics task from data.yaml and label shape."""
    yaml_task = str(yaml_data.get("task", "")).strip().lower()
    if yaml_task in {"detect", "pose", "segment"}:
        return yaml_task

    model_name = str(yaml_data.get("model", "")).strip()
    if model_name:
        model_task = _model_task(model_name)
        if model_task != "detect":
            return model_task

    if "kpt_shape" in yaml_data:
        return "pose"

    label_line = _first_label_line(yaml_data, dataset_dir)
    if label_line:
        parts = label_line.split()
        if len(parts) > 5:
            return "segment"
    return "detect"


def _select_model_name(yaml_data, task, requested_model):
    """Choose model with YOLO26 defaults when GUI/CLI is set to auto."""
    requested = str(requested_model or "").strip()
    if requested and requested.lower() not in {"auto", AUTO_MODEL_LABEL.lower()}:
        return requested

    yaml_model = str(yaml_data.get("model", "")).strip()
    if yaml_model and _model_task(yaml_model) == task:
        return yaml_model
    return DEFAULT_MODEL_BY_TASK.get(task, DEFAULT_MODEL_BY_TASK["detect"])


def _coerce_int(value, default):
    try:
        return int(value)
    except (TypeError, ValueError):
        return int(default)


def _coerce_batch(value, default):
    try:
        numeric = float(value)
    except (TypeError, ValueError):
        return default
    return int(numeric) if numeric.is_integer() else numeric


def _resolve_training_config(
    yaml_file,
    *,
    task="auto",
    model="auto",
    epochs=None,
    batch=None,
    imgsz=None,
    device=None,
    project=None,
    name="yolo_training",
):
    """Resolve, validate, and normalize GUI/CLI training settings."""
    yaml_file = os.path.abspath(str(yaml_file))
    if not os.path.exists(yaml_file):
        raise FileNotFoundError(f"YAML file not found: {yaml_file}")

    yaml_data = _load_yaml(yaml_file)
    dataset_dir = os.path.dirname(yaml_file)
    missing = [field for field in ("train", "val", "names") if field not in yaml_data]
    if missing:
        raise ValueError(f"Missing required YAML fields: {missing}")

    requested_task = str(task or "auto").strip().lower()
    if requested_task not in TASK_CHOICES:
        raise ValueError(f"Invalid task '{task}'. Use one of: {', '.join(TASK_CHOICES)}")
    resolved_task = (
        _detect_task_from_yaml(yaml_data, dataset_dir)
        if requested_task == "auto"
        else requested_task
    )

    model_name = _select_model_name(yaml_data, resolved_task, model)
    _validate_model_task_match(resolved_task, model_name)
    train_path = _resolve_yaml_path_entry(yaml_data, dataset_dir, "train")
    val_path = _resolve_yaml_path_entry(yaml_data, dataset_dir, "val")
    if not os.path.exists(train_path):
        raise FileNotFoundError(f"Train images not found: {train_path}")
    if not os.path.exists(val_path):
        raise FileNotFoundError(f"Val images not found: {val_path}")

    resolved_epochs = _coerce_int(
        epochs if epochs is not None else yaml_data.get("epochs", 100), 100
    )
    resolved_batch = _coerce_batch(batch if batch is not None else yaml_data.get("batch", 16), 16)
    resolved_imgsz = _coerce_int(imgsz if imgsz is not None else yaml_data.get("imgsz", 640), 640)

    if device is None:
        resolved_device = str(yaml_data.get("device") or _default_device())
    else:
        device_text = str(device).strip()
        resolved_device = _default_device() if device_text.lower() == "auto" else device_text

    dataset_base = str(yaml_data.get("path") or dataset_dir).strip() or dataset_dir
    if not os.path.isabs(dataset_base):
        dataset_base = os.path.join(dataset_dir, dataset_base)
    dataset_root = os.path.abspath(os.path.normpath(dataset_base))
    resolved_project = os.path.abspath(str(project or os.path.join(dataset_root, "runs")))
    resolved_name = str(name or yaml_data.get("name") or "yolo_training").strip() or "yolo_training"

    return TrainingConfig(
        yaml_file=yaml_file,
        dataset_dir=dataset_root,
        train_path=train_path,
        val_path=val_path,
        task=resolved_task,
        model_name=model_name,
        epochs=resolved_epochs,
        batch=resolved_batch,
        imgsz=resolved_imgsz,
        device=resolved_device,
        project=resolved_project,
        name=resolved_name,
    )


def _model_path_for_training(model_name):
    """Return local model path, downloading named Ultralytics weights into vaila/models if needed."""
    model_text = os.path.expanduser(str(model_name).strip())
    if not model_text:
        raise ValueError("Empty YOLO model name.")
    if os.path.isabs(model_text) or os.sep in model_text:
        return model_text

    models_dir = os.path.join(os.path.dirname(__file__), "models")
    os.makedirs(models_dir, exist_ok=True)
    local_path = os.path.join(models_dir, model_text)
    if os.path.exists(local_path):
        return local_path

    current_dir = os.getcwd()
    try:
        os.chdir(models_dir)
        YOLO(model_text)
    finally:
        os.chdir(current_dir)
    return local_path if os.path.exists(local_path) else model_text


def _parse_extra_train_args(extra_items):
    """Parse ``--arg key=value`` values for Ultralytics train overrides."""
    parsed = {}
    for item in extra_items or []:
        if "=" not in item:
            raise ValueError(f"Extra train argument must be key=value, got: {item}")
        key, value = item.split("=", 1)
        key = key.strip()
        value = value.strip()
        if not key:
            raise ValueError(f"Empty train argument key in: {item}")
        lower = value.lower()
        if lower in {"true", "false"}:
            parsed[key] = lower == "true"
            continue
        if lower in {"none", "null"}:
            parsed[key] = None
            continue
        with contextlib.suppress(ValueError):
            parsed[key] = int(value)
            continue
        with contextlib.suppress(ValueError):
            parsed[key] = float(value)
            continue
        parsed[key] = value
    return parsed


def train_yolo_dataset(
    yaml_file,
    *,
    task="auto",
    model="auto",
    epochs=None,
    batch=None,
    imgsz=None,
    device=None,
    project=None,
    name="yolo_training",
    exist_ok=True,
    dry_run=False,
    extra_train_args=None,
):
    """Train an Ultralytics YOLO model from a vailá/getpixelvideo YOLO dataset."""
    config = _resolve_training_config(
        yaml_file,
        task=task,
        model=model,
        epochs=epochs,
        batch=batch,
        imgsz=imgsz,
        device=device,
        project=project,
        name=name,
    )
    if dry_run:
        return config

    model_path = _model_path_for_training(config.model_name)
    yolo_model = YOLO(model_path)
    train_kwargs = {
        "data": config.yaml_file,
        "epochs": config.epochs,
        "batch": config.batch,
        "imgsz": config.imgsz,
        "device": config.device,
        "project": config.project,
        "name": config.name,
        "exist_ok": exist_ok,
        "verbose": True,
    }
    train_kwargs.update(extra_train_args or {})
    return yolo_model.train(**train_kwargs)


# --- Class to redirect the console output to the Text Widget ---
class ConsoleRedirector:
    """Redirects stdout to both GUI and terminal."""

    def __init__(self, text_widget):
        self.text_widget = text_widget
        self.original_stdout = sys.stdout

    def write(self, text):
        # Write to GUI
        self.text_widget.insert(tk.END, text)
        self.text_widget.see(tk.END)
        self.text_widget.update_idletasks()

        # Also write to original terminal
        self.original_stdout.write(text)
        self.original_stdout.flush()

    def flush(self):
        self.original_stdout.flush()


