    def start_training_thread(self):
        """Starts training in a separate thread."""
        self._print_gui_state("Start Training clicked")
        if not self.yaml_path.get():
            dataset_folder = self.dataset_path.get()
            if dataset_folder:
                print(f"[yolotrain] Start clicked without YAML. Searching folder: {dataset_folder}")
                if self._select_best_yaml_from_folder(
                    dataset_folder, "Start Training auto-discovery"
                ):
                    print(f"[yolotrain] Auto-selected YAML before training: {self.yaml_path.get()}")
            if not self.yaml_path.get():
                messagebox.showerror(
                    "Error",
                    "No data.yaml selected. Browse a dataset folder or data.yaml first.",
                )
                return

        if not self._validate_existing_yaml(self.yaml_path.get()):
            return
        if not self._confirm_suspicious_pose_dataset():
            return

        self.start_button.config(state=tk.DISABLED)
        self.console.delete("1.0", tk.END)
        self.console.insert(tk.END, "Starting training...\n")

        training_thread = threading.Thread(target=self._run_training)
        training_thread.daemon = True
        training_thread.start()

