def _find_dataset_yaml_candidates(dataset_path, max_depth=2):
    """Find nearby Ultralytics data.yaml files for GUI folder selections."""
    root = pathlib.Path(str(dataset_path)).expanduser()
    if root.is_file() and root.suffix.lower() in {".yaml", ".yml"}:
        return [str(root.resolve())]
    if not root.is_dir():
        return []

    skip_dirs = {
        ".git",
        "__pycache__",
        "all_labels",
        "images",
        "labels",
        "runs",
        "test",
        "train",
        "val",
        "weights",
    }
    candidates = {}

    def add(path):
        if path.name.lower() in {"data.yaml", "data.yml"}:
            with contextlib.suppress(OSError):
                candidates[str(path.resolve())] = path.stat().st_mtime

    def walk(directory, depth):
        if depth > max_depth:
            return
        try:
            children = list(directory.iterdir())
        except OSError as exc:
            print(f"[yolotrain] Could not scan {directory}: {exc}")
            return
        for child in children:
            if child.is_file():
                add(child)
            elif child.is_dir() and depth < max_depth and child.name not in skip_dirs:
                walk(child, depth + 1)

    walk(root, 0)
    return sorted(candidates, key=lambda path: (candidates[path], path), reverse=True)


def _find_nearby_toml_candidates(dataset_path, max_depth=1):
    """Find nearby TOML files only to explain that yolotrain needs data.yaml."""
    root = pathlib.Path(str(dataset_path)).expanduser()
    if root.is_file():
        root = root.parent
    if not root.is_dir():
        return []
    candidates = []

    def walk(directory, depth):
        if depth > max_depth:
            return
        try:
            children = list(directory.iterdir())
        except OSError:
            return
        for child in children:
            if child.is_file() and child.suffix.lower() == ".toml":
                candidates.append(str(child.resolve()))
            elif (
                child.is_dir()
                and depth < max_depth
                and child.name not in {"train", "val", "test", "images", "labels", "runs"}
            ):
                walk(child, depth + 1)

    walk(root, 0)
    return sorted(candidates)


