def main(argv=None):
    """CLI entry point. No args opens the GUI for backwards compatibility."""
    parser = build_arg_parser()
    args = parser.parse_args(argv)
    if args.print_guide:
        print(_read_training_guide_text())
        return 0
    if args.guide:
        uri = _training_help_uri()
        print(_training_quick_guide_text())
        print(f"Markdown guide: {TRAINING_HELP_MD_PATH}")
        print(f"HTML guide: {uri}")
        if os.environ.get("DISPLAY") or sys.platform in {"darwin", "win32"}:
            opened = webbrowser.open_new_tab(uri)
            print(f"Browser open requested: {opened}")
        else:
            print("No graphical display detected; guide was printed in terminal.")
        return 0
    if args.gui or not args.data:
        run_yolotrain_gui()
        return 0

    try:
        extra_args = _parse_extra_train_args(args.arg)
        config = train_yolo_dataset(
            args.data,
            task=args.task,
            model=args.model,
            epochs=args.epochs,
            batch=args.batch,
            imgsz=args.imgsz,
            device=args.device,
            project=args.project,
            name=args.name,
            exist_ok=args.exist_ok,
            dry_run=args.dry_run,
            extra_train_args=extra_args,
            allow_suspicious_pose=args.allow_suspicious_pose,
        )
    except Exception as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1
    if args.dry_run:
        print("Resolved YOLO training config:")
        print(f"  data: {config.yaml_file}")
        print(f"  dataset: {config.dataset_dir}")
        print(f"  task: {config.task}")
        print(f"  model: {config.model_name}")
        print(f"  train: {config.train_path} ({_image_count(config.train_path)} images)")
        print(f"  val: {config.val_path} ({_image_count(config.val_path)} images)")
        print(f"  epochs: {config.epochs}")
        print(f"  batch: {config.batch}")
        print(f"  imgsz: {config.imgsz}")
        print(f"  device: {config.device}")
        print(f"  project: {config.project}")
        print(f"  name: {config.name}")
        print("Equivalent training command:")
        print(f"  {_format_training_cli_command(config)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
