class _GuiStreamTee:
    """Thread-safe tee: terminal immediately + batched Tk console updates."""

    def __init__(self, text_widget, tk_root, original_stream):
        self._widget = text_widget
        self._root = tk_root
        self._original = original_stream
        self._pending: list[str] = []
        self._flush_job: object | None = None

    def write(self, text: str) -> int:
        if not text:
            return 0
        self._original.write(text)
        self._original.flush()
        # Keep raw tqdm ``\r`` progress in the terminal only (avoid GUI spam).
        if "\r" in text and "\n" not in text:
            return len(text)
        clean = text.replace("\r", "")
        if not clean:
            return len(text)
        self._pending.append(clean)
        if self._flush_job is None:
            self._flush_job = self._root.after(50, self._flush_pending)
        return len(text)

    def _flush_pending(self) -> None:
        self._flush_job = None
        if not self._pending:
            return
        chunk = "".join(self._pending)
        self._pending.clear()
        self._widget.insert(tk.END, chunk)
        self._widget.see(tk.END)

    def flush(self) -> None:
        self._original.flush()
        if self._flush_job is not None:
            with contextlib.suppress(tk.TclError):
                self._root.after_cancel(self._flush_job)
            self._flush_job = None
        self._flush_pending()


@contextlib.contextmanager
def _tee_training_streams(text_widget, tk_root):
    """Redirect stdout and stderr to terminal and GUI during training."""
    out_tee = _GuiStreamTee(text_widget, tk_root, sys.__stdout__)
    err_tee = _GuiStreamTee(text_widget, tk_root, sys.__stderr__)
    old_out, old_err = sys.stdout, sys.stderr
    sys.stdout, sys.stderr = out_tee, err_tee
    try:
        yield out_tee, err_tee
    finally:
        sys.stdout, sys.stderr = old_out, old_err
        out_tee.flush()
        err_tee.flush()


