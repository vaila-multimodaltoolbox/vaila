def build_arg_parser():
    """Build CLI parser for headless YOLO training."""
    parser = argparse.ArgumentParser(
        description="Train YOLO26/Ultralytics models from vailá getpixelvideo/SAM datasets."
    )
    parser.add_argument("--gui", action="store_true", help="Open the Tkinter GUI.")
    parser.add_argument("--data", "-d", help="Path to data.yaml.")
    parser.add_argument(
        "--task",
        choices=TASK_CHOICES,
        default="auto",
        help="YOLO task. auto reads data.yaml (kpt_shape -> pose).",
    )
    parser.add_argument(
        "--model",
        default="auto",
        help="Model name/path. auto uses YOLO26 default for the detected task.",
    )
    parser.add_argument("--epochs", type=int, help="Training epochs override.")
    parser.add_argument("--batch", type=float, help="Batch size override; -1 for Ultralytics auto.")
    parser.add_argument("--imgsz", type=int, help="Image size override.")
    parser.add_argument(
        "--device",
        help="Device override: auto, cpu, mps, 0, 0,1, etc. Defaults to YAML or auto.",
    )
    parser.add_argument("--project", help="Output project directory. Default: <dataset>/runs.")
    parser.add_argument("--name", default="yolo_training", help="Run name under project directory.")
    parser.add_argument(
        "--exist-ok",
        action="store_true",
        default=True,
        help="Allow overwriting/reusing project/name (default).",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Validate YAML and print resolved config without downloading/training.",
    )
    parser.add_argument(
        "--arg",
        action="append",
        default=[],
        metavar="KEY=VALUE",
        help="Extra Ultralytics train override. May be repeated, e.g. --arg workers=4.",
    )
    return parser


def main(argv=None):
    """CLI entry point. No args opens the GUI for backwards compatibility."""
    parser = build_arg_parser()
    args = parser.parse_args(argv)
    if args.gui or not args.data:
        run_yolotrain_gui()
        return 0

    try:
        extra_args = _parse_extra_train_args(args.arg)
        config = train_yolo_dataset(
            args.data,
            task=args.task,
            model=args.model,
            epochs=args.epochs,
            batch=args.batch,
            imgsz=args.imgsz,
            device=args.device,
            project=args.project,
            name=args.name,
            exist_ok=args.exist_ok,
            dry_run=args.dry_run,
            extra_train_args=extra_args,
        )
    except Exception as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1
    if args.dry_run:
        print("Resolved YOLO training config:")
        print(f"  data: {config.yaml_file}")
        print(f"  dataset: {config.dataset_dir}")
        print(f"  task: {config.task}")
        print(f"  model: {config.model_name}")
        print(f"  train: {config.train_path} ({_image_count(config.train_path)} images)")
        print(f"  val: {config.val_path} ({_image_count(config.val_path)} images)")
        print(f"  epochs: {config.epochs}")
        print(f"  batch: {config.batch}")
        print(f"  imgsz: {config.imgsz}")
        print(f"  device: {config.device}")
        print(f"  project: {config.project}")
        print(f"  name: {config.name}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
